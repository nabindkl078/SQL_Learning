""" A travel company wants to fly a plane to the Bahamas. Flying the plane costs 5000 dollars. So far, 29 people have signed up for
the trip. If the company charges 200 dollars per ticket, what is the profit made by the company? Create variable for each numeric quantity and use appropriate arithmetic operations."""
number_of_people = 29
cost_of_plane = 5000
charge_per_ticket = 200
total_charge_paid = number_of_people * charge_per_ticket
profit =  total_charge_paid - cost_of_plane 
print("The total amount of profit made by the company is ", str(profit) + " dollars")
